public class Main {
    public static void main(String[] args) {

        // enteros
        byte variableByte = 4;
        short variableShort = 9;
        int variableInt = 1234;
        long variableLong = 12346689;

        System.out.println(variableByte);
        System.out.println(variableShort);
        System.out.println(variableInt);
        System.out.println(variableLong);

        System.out.println();
        // decimales
        float variableFloat = 12.4f;
        double variableDouble = 566.9d;
        System.out.println(variableFloat);
        System.out.println(variableDouble);

        System.out.println();

        // booleans
        boolean variableBoolean = true;
        boolean variableBoolean1 = false;
        System.out.println(variableBoolean);
        System.out.println(variableBoolean1);

        System.out.println();

        // texto
        char variableChar = 'A';
        String variableString = "Andres Felipe Rivera";
        System.out.println(variableChar);
        System.out.println(variableString);


    }

}